package AllUnits;

public class UnitBean {
	
	String B,BP,O,OP,AN,AP,AB,ONv;

	public UnitBean(String b, String bP, String o, String oP, String aN, String aP, String aB, String oNv) {
		super();
		B = b;
		BP = bP;
		O = o;
		OP = oP;
		AN = aN;
		AP = aP;
		AB = aB;
		ONv = oNv;
	}

	public String getB() {
		return B;
	}

	public void setB(String b) {
		B = b;
	}

	public String getBP() {
		return BP;
	}

	public void setBP(String bP) {
		BP = bP;
	}

	public String getO() {
		return O;
	}

	public void setO(String o) {
		O = o;
	}

	public String getOP() {
		return OP;
	}

	public void setOP(String oP) {
		OP = oP;
	}

	public String getAN() {
		return AN;
	}

	public void setAN(String aN) {
		AN = aN;
	}

	public String getAP() {
		return AP;
	}

	public void setAP(String aP) {
		AP = aP;
	}

	public String getAB() {
		return AB;
	}

	public void setAB(String aB) {
		AB = aB;
	}

	public String getONv() {
		return ONv;
	}

	public void setONv(String oNv) {
		ONv = oNv;
	}
	

}
